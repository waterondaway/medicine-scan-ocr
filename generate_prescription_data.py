def format_thai_date(date):
    thai_months = [
        "มกราคม", "กุมภาพันธ์", "มีนาคม", "เมษายน", "พฤษภาคม", 
        "มิถุนายน", "กรกฎาคม", "สิงหาคม", "กันยายน", "ตุลาคม", 
        "พฤศจิกายน", "ธันวาคม"
    ]
    day = date.day
    month = thai_months[date.month - 1]
    year = date.year + 543
    return f"{day} {month} {year}"

